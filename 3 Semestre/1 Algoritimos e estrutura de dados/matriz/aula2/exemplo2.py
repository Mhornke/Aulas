# Repetições

# range(10): vai de 0 até 9
for i in range(10):
  print(i)

# range(1, 10): vai de 1 até 9
for i in range(1, 10):
  print(i)

# range(1, 10, 2): vai de 1 até 9, pulando de 2 em 2
for i in range(1, 10, 2):
  print(i)

# range(10, 0, -1): vai de 10 até 1, decrescendo
for i in range(10, 0, -1):
  print(i)

